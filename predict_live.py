# Translation Engine (modular stub for future language support)
# ═══════════════════════════════════════════════════════════════════════════

class TranslationEngine:
    """
    Modular translation component for future multilingual TTS support.

    Currently acts as a pass-through (English → English). To enable
    Tamil translation in the future, implement the translate() method
    using googletrans, deep-translator, or a local model.

    Usage:
        translator = TranslationEngine(target_lang="ta")
        tamil_text = translator.translate("HELLO")  # → "வணக்கம்"
    """

    # Predefined dictionary for common ASL words → Tamil
    # This avoids API calls for frequently used phrases
    TAMIL_DICT = {
        "HELLO": "வணக்கம்",
        "THANK YOU": "நன்றி",
        "PLEASE": "தயவுசெய்து",
        "YES": "ஆம்",
        "NO": "இல்லை",
        "GOODBYE": "போய் வருகிறேன்",
        "SORRY": "மன்னிக்கவும்",
        "HELP": "உதவி",
        "I LOVE YOU": "நான் உன்னை காதலிக்கிறேன்",
    }

    def __init__(self, target_lang="en", enabled=False):
        """
        Args:
            target_lang: ISO 639-1 language code ("en", "ta", etc.)
            enabled: Set True to activate translation. False = pass-through.
        """
        self.target_lang = target_lang
        self.enabled = enabled

    def translate(self, text: str) -> str:
        """
        Translate the input text to the target language.

        Currently returns the original text unchanged.
        Override this method for actual translation.
        """
        if not self.enabled or self.target_lang == "en":
            return text

        # Check local dictionary first (for Tamil)
        if self.target_lang == "ta":
            upper_text = text.upper().strip()
            if upper_text in self.TAMIL_DICT:
                return self.TAMIL_DICT[upper_text]

        # Placeholder for API-based translation
        # Future: from deep_translator import GoogleTranslator
        # return GoogleTranslator(source='en', target=self.target_lang).translate(text)
        return text


