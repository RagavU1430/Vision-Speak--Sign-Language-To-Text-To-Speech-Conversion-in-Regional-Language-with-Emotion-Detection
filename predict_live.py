"""
Sign Language A-Z  --  Live Webcam Prediction + Text-to-Speech
===============================================================
Uses MediaPipe Hands + the trained MLP model to recognise
ASL alphabet signs (A-Z) from your webcam in real time,
with integrated pyttsx3 text-to-speech output.

Temporal Logic (prevents duplicate letters):
  - 20-frame majority voting (>80% consistency required)
  - 90% model confidence threshold
  - 1.5-second hold timer before committing a letter
  - Hand-reset lock: must remove hand or show a different sign
    before the same letter can be added again

Text-to-Speech:
  - pyttsx3 engine (singleton, non-blocking via background thread)
  - ENTER speaks the full sentence buffer
  - Duplicate speech protection while TTS is active
  - Real-time status display: READY / SPEAKING / COMPLETED
  - AUTO_CLEAR option to clear sentence after speaking

Controls:
  Q / ESC   -> Quit
  C         -> Clear word buffer AND sentence buffer
  SPACE     -> Finish current word, push to sentence buffer
  BKSP      -> Delete last character from word buffer
  ENTER     -> Speak the accumulated sentence aloud

Requirements:
  - models/mlp_model.pkl
  - models/label_encoder.pkl
  - models/scaler.pkl
  (Generated by train_mlp.py)
  - pyttsx3  (pip install pyttsx3)
"""

