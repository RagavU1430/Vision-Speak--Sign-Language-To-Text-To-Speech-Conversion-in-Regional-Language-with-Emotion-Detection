def load_model():
    """Load the trained MLP model, label encoder, and scaler."""
    for path in (MODEL_PATH, ENCODER_PATH, SCALER_PATH):
        if not os.path.exists(path):
            print(f"[ERROR] Missing file: {path}")
            print("        Run train_mlp.py first to generate model artefacts.")
            sys.exit(1)

    model = joblib.load(MODEL_PATH)
    le = joblib.load(ENCODER_PATH)
    scaler = joblib.load(SCALER_PATH)
    print("[OK] Model, encoder, and scaler loaded.")
    return model, le, scaler


def extract_landmarks(hand_landmarks) -> np.ndarray:
    """Convert MediaPipe hand landmarks to a flat 63-element array."""
    coords = []
    for lm in hand_landmarks.landmark:
        coords.extend([lm.x, lm.y, lm.z])
    return np.array(coords, dtype=np.float32).reshape(1, -1)


